def pr(mapa, row, column, path): # row, column - trenutna lokacija
  if row == column == 10: return path
  if mapa[row][column]: return ''
  mapa[row][column] = 1
  return pr(mapa, row+1, column, path+'S') + pr(mapa, row, column+1, path+'E') + pr(mapa, row-1, column, path+'N') + pr(mapa, row, column-1, path+'W')

def checkio(maze_map):
  return pr(maze_map, 1,1, '')

